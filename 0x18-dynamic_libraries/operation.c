int add(int a, int b);
int sub(int a, int b);
int div(int a, int b);

/**
 * add - function
 * @a:int
 * @b:int
 * Return:sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 ** sub - function
 ** @a:int
 ** @b:int
 ** Return:sum
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 ** div - function
 ** @a:int
 ** @b:int
 ** Return:sum
 */
int div(int a, int b)
{
	return (a / b);
}
