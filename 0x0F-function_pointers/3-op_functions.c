#include "3-calc.h"
/**
 * op_add - function
 * @a:p1
 * @b:p2
 * Return:sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function
 * @a:p1
 * @b:p2
 * Return:substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function
 * @a:p1
 * @b:p2
 * Return:multipilication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function
 * @a:p1
 * @b:p2
 * Return:division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function
 * @a:p1
 * @b:p2
 * Return:mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
